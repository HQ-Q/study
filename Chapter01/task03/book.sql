CREATE TABLE books (
                       id INT AUTO_INCREMENT PRIMARY KEY,  -- 主键，自增，唯一标识书籍
                       title VARCHAR(255) NOT NULL,        -- 书名，非空，最长255字符（支持较长书名）
                       author VARCHAR(100) NOT NULL,       -- 作者名，非空，最长100字符
                       price DECIMAL(10, 2) NOT NULL       -- 价格，保留2位小数，非空（范围支持0.00至99999999.99）
);
